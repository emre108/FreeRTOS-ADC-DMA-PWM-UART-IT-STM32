/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
		char *str="HAL_ADC is starting\r\n";
		HAL_UART_Transmit(&huart6,(uint8_t*)str,strlen(str),1000);
		for(int i=0;i<20;i++){
			HAL_GPIO_TogglePin(GPIOD,GPIO_PIN_15);
			osDelay(100);
		}
	osSemaphoreRelease(myBinarySem01Handle);
	HAL_ADC_Start_DMA(&hadc1,(uint32_t*)ADCbuffer,2);
	osEvent adcGet;
	osEvent	adcGet2;
  /* Infinite loop */
  for(;;)
  {
		adcGet=osMessageGet(myQueue01Handle,osWaitForever);
		adcGet2=osMessageGet(myQueue02Handle,osWaitForever);
		if(pwm_Config==high){	
			if(adcGet.status == osEventMessage){
				htim4.Instance-> CCR1 =100*adcGet.value.v/4095;
			}
		  if(adcGet2.status == osEventMessage){
				htim4.Instance-> CCR2 =100*adcGet2.value.v/4095;
			}
		}
		else if(pwm_Config==low){
			htim4.Instance-> CCR1 =100*adcGet.value.v/4095;
			htim4.Instance-> CCR2 =100*adcGet2.value.v/4095;
		}
			sprintf(Streaming_real, "channel-1:%d channel-2:%d  \r\n", adcGet.value.v,adcGet2.value.v);
			HAL_UART_Transmit(&huart6,(uint8_t*)Streaming_real,strlen(Streaming_real),1000);
			osSemaphoreRelease(myBinarySem01Handle);
			osDelay(10);
  }
  /* USER CODE END 5 */ 
}

